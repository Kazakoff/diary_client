            <p>
                Физическая подготовленностьчеловека характеризуется уровнем развития его
                физических качеств: силы, быстроты, выносливости, гибкости, ловкости.
                Уровень развития физического качества может быть средним, высоким или очень
                высоким, низким или очень низким и др. Наряду с многими другими факторами
                (питание, использование оздоровительных сил природы и гигиенических
                факторов) основным средством повышения уровня развития физических качеств
            или поддержания их оптимального уровня являются физические упражнения.    <strong></strong>
            </p>
            <p>
                <strong></strong>
            </p>
            <p>
                <strong>2.1 Правила выполнения тестовых и контрольных упражнений</strong>
                <strong></strong>
            </p>
            <p>
                <strong></strong>
            </p>
            <p>
                Перед выполнением любого теста выполняются подготовительные упражнения,
                оказывающие разогревающее и разминочное воздействие на мышцы, связки и
                суставы, участвующие в движении.
            </p>
            <p>
                <strong>1. Сгибание и разгибание рук в упоре лежа.</strong>
                Студенты выполняют упражнение в упоре лежа на полу. Туловище прямое, руки в
                локтевом суставе сгибаются до прямого угла. Подсчитывается количество
                движений, регистрируемых по касаниям грудью счетного устройства –
                платформы. Тест характеризует силовую выносливость.<strong></strong>
            </p>
            <p>
                <strong>2. Лежа на спине, удержание ног над полом.</strong>
                Лежа на спине, руки вдоль туловища, ладони на полу, поднять ноги над полом
                на 20–25 см иудерживать их в этом положении. Регистрируется время до
                касания стопами пола. Тест характеризует статическую силовую выносливость.
            </p>
            <p>
                <strong>3. Теппинг</strong>
                –<strong>тест.</strong> Сидя за столом, рука с карандашом на листе бумаги.
                В течение 10 с, выполняя движения только кистью с максимальной скоростью,
                на лист бумаги в хаотичном порядке наносятся карандашом точки.
                Подсчитывается количество точек. Подсчитывать точки рекомендуется, соединяя
                их карандашом непрерывной линией, чтобы не сбиться. Количество нанесенных
                точек характеризует быстроту движений.<strong></strong>
            </p>
            <p>
                <strong>4. Бег на месте в течение 10 с.</strong>
                Подсчитывается количество шагов, выполненное за 10 с. Руки работают как в
                беге, колени сгибаются до прямого угла. Для ориентира и контроля высоты
                поднимания колен, перед тестируемым натягивается резиновый бинт (лента) на
                высоте поднятого бедра ноги, согнутой в коленном суставе до прямого угла.
                Во время бега тестируемый касается верхней поверхностью бедра этого
                ориентира. Результат удобнее подсчитывать по одной ноге, затем полученное
                число удвоить. Тест характеризует скоростные способности.
            </p>
            <p>
                <strong>5. Полуприсед в статике. </strong>
                Принять исходное положение полуприсед в упоре спиной о стену. Ноги согнуты
                в коленях под углом 90°, руки вниз параллельно стене. Фиксируется время
                удержания данного положения в секундах. Тест характеризует статическую
                силовую выносливость нижних конечностей.
            </p>
            <p>
                <strong>6. Подтягивание на перекладине. </strong>
                Подтягиваясь, хватом кистей сверху, подбородок поднимается выше
                перекладины, рывковые движения ногами не допускаются. Выполняют юноши. Тест
                характеризует силовые способности. <strong></strong>
            </p>
            <p>
                <strong>7. «Планка». У</strong>
                пражнение выполняется на гимнастическом коврике. Принять исходное
                положение: упор лежа на согнутых руках (согнуты в локтях до угла 90°) и
                носках ног, тело прямое в виде ровной линии от головы до пят, не провисает,
                брюшной пресс напряжен. Фиксируется время удержания исходного положения
            данной позы в секундах. Определяется статическая выносливость.    <strong></strong>
            </p>
            <p>
                <strong>
                    8. Наклон вперед из <a name="_GoBack"></a>исходного положения сидя.
                </strong>
                Сидя на полу, ноги выпрямлены в коленях (чтобы не сгибались во время
                наклонов, удерживаются помощниками), ступни ног расположены параллельно на
                ширине 10–15 см, на линии, принятой за нулевую отметку. В спортивном зале
                разметка может быть нарисована на полу, а для точного фиксирования
                положения стоп на нулевой линии могут использоваться съемные упоры в виде
                двух деревянных брусков. На время тестирования такой брусок двумя
                металлическими штырями (из толстой проволоки) вставляется в подготовленные
                отверстия пола, стороной к которой прилегает стопа тестируемого точно на
                нулевой линии. Выполняется два предварительных наклона, ладони двигаются
                вдоль линейки измерения. При третьем наклоне максимально наклониться и
                удерживать касание линейки измерения в течение 2 с. Расстояние (в см)
                касания за нулевой линией записывается в протокол со знаком плюс, до
                нулевой линии – со знаком минус, касание нулевой линии – регистрируется как
                «0». Определяется общая гибкость. <strong></strong>
            </p>
            <p>
                <strong>9. Прыжок в длину с места. </strong>
                Предварительно выполняются упражнения для разминки ног и голеностопных
                суставов. Не наступая на линию старта, выполняется прыжок максимально
                дальше вперед. Результат измеряется от линии старта до ближайшей к ней
                пятки в см. Точность измерения до 1 см.<strong></strong>
            </p>
            <p>
                <strong></strong>
            </p>
            <p>
                <strong>
                    2.2 Контрольные упражнения по освоению технических приемов игры в
                    баскетбол
                </strong>
            </p>
            <p>
                <strong>1. Штрафные броски в корзину.</strong>
                Подсчитывается количество попаданий в корзину со штрафной линии из 5–ти
                попыток (юноши), 4–х попыток (девушки).
            </p>
            <p>
                <strong>2. Ведение – 2шага – бросок (двухшажная техника).</strong>
                Выполняется 2 шага и бросок в кольцо. Оценивается количество попаданий из
                5–ти технически правильно исполненных попыток (юноши), 4–х попыток
                (девушки).
            </p>
            <p>
                <strong>3. Техника быстрого ведения мяча. </strong>
                Выполняется ведение мяча от лицевой линии до противоположной лицевой линии
                баскетбольной площадки. Оценивается время выполнения ведения.
            </p>
            <p>
                <strong></strong>
            </p>
            <p>
                <strong>
                    2.3 Контрольные упражнения и тесты по освоению технических приемов игры
                    в волейбол
                </strong>
            </p>
            <p>
                <strong></strong>
            </p>
            <p>
                <strong>1. Передача мяча двумя руками над собой.</strong>
                Высота полета мяча не менее 0,5 м. Оценивается количество технически
                правильно выполненных передач с 3–х попыток.
            </p>
            <p>
                <strong>2. Верхняя передача мяча в парах.</strong>
                В парах оценивается количество технически правильно выполненных передач.
                Расстояние между игроками 6– 8 м.
            </p>
            <p>
                <strong>3. Нижняя передача мяча в парах.</strong>
                В парах оценивается количество технически правильно выполненных передач.
                Расстояние между игроками 6– 8 м. У одного партнера любая передача, у
                второго только нижняя передача.
            </p>
            <p>
                <strong>
                    4. Верхняя подача мяча через сетку (юноши). Верхняя, нижняя, боковая
                    подача мяча через сетку (девушки).
                </strong>
                Оценивается количество технически правильно выполненных подач через сетку
                на противоположную площадку.
            </p>
            <p>
                <strong></strong>
            </p>
