            <p align="center">
                <strong>3 Здоровый образ жизни и отношение к своему здоровью</strong>
            </p>
            <p align="center">
                <strong></strong>
            </p>
            <p>
                <strong>3.1 Слагаемые и правила здорового образа жизни</strong>
            </p>
            <p>
                <strong>1. Ежедневная двигательная активность.</strong>
                Оптимальнаядлительность интенсивнойдвигательной активности совершаемой в
                течение дня должна приближаться к 3–4 часам. Старайтесь каждый день
                набирать ее, как только для этого предоставляется возможность. Выполняйте
                утром упражнения для укрепления и развития своего двигательного аппарата, а
                между работой за компьютером или учебниками – для активного отдыха,
                заменяйте пользование лифтом ходьбой, реже пользуйтесь транспортом, ходите
                в университет и из университета пешком (хотя бы некоторую часть маршрута,
                если расстояние слишком велико). При отсутствии медицинских
                противопоказаний планируйте и осуществляйте ежедневно или хотя бы через
                день специально организованную физическую активность в течение 30–60 минут,
                сопровождающуюся учащением сердечных сокращений и частоты дыхания.
                Известно, что только после 30–40 минут непрерывной умеренной физической
                нагрузки, организм начинает сжигать жировые запасы. Это могут быть занятия
                физическими упражнениями дома или в организованных группах, игры, пробежки,
                и просто пешие прогулки по своей продолжительности и темпу сопровождающиеся
                физической нагрузкой, оказывающей оздоровительное воздействие на организм
                (повышение ЧСС в диапазоне 120–140 уд/мин).
            </p>
            <p>
                <strong>2. Правильное питание.</strong>
                Включите в свой ежедневный рацион питания фрукты, овощи, блюда, содержащие
                цельные зерна, богатые клетчаткой, витаминами и минералами. Необходимо
                минимизировать, а правильнее отказаться от употребления фастфуда,
                газированных напитков, полуфабрикатови прочих продуктов, содержащих
                искусственные пищевые добавки: усилители вкуса, красители, консерванты,
                ароматизаторы и др. Этому помогает при покупке продуктов обязательное
                ознакомление на этикетке с его составом. Стараться не есть пережаренную
                пищу, ограничить употребление животных жиров, сахара, соли, кондитерских
                изделий. Принимать пищу не реже 3 раз в день, еще лучше 4–5 раз небольшими
                порциями. Избегайте переедания!Последний прием пищи осуществляйте не
                позднее 4 часов до сна. Старайтесь в течение дня выпивать 1,5–2 литра
                чистой воды (прошедшей очистку или бутилированной).
            </p>
            <p>
                <strong>3. Полноценный сон и отдых.</strong>
                Ночной сон должендлиться не менее восьми часов. Старайтесь ложиться спать и
                вставать в одно и то же время. Если есть возможность, позвольте себе час
                дневного сна или просто 20–30 минутный отдых в положении лежа. Различные
                исследования показывают, что самый крепкий сон (соответственно наиболее
                ценный для отдыха организма) приходится на промежуток времени с 22 часов
                до01 часа, с02 часов и до 04 часов, и между 13 и 15 часами. Естественно,
                чтобы войти в фазу крепкого сна, ложиться спать и засыпать нужно раньше
                времени начала этой фазы.
            </p>
            <p>
                <strong>4. Позитивное настроение.</strong>
                Отгоняйте прочь негативные мысли, не впадайте в уныние и плохое настроение.
                Если не можете справиться с этим сами, измените ситуацию через общение,
                посещение мероприятий и мест, заряжающих позитивом, приятными впечатлениями
                и веселым настроением. Не давайте свободу своей ненависти и злобе,
                обращайтесь к людям с добром и сами будете чаще его получать от других.
            </p>
            <p>
                <strong>5. Чистый воздух.</strong>
                Проветривайте жилое помещение ежедневно, обязательно в течение 5–10 минут
                перед сном, чаще бывайте в парковых зонах, на природе, избегайте мест с
                высокой концентрацией выхлопных газов, табачного дыма, нахождения рядом с
                лицами, которые в этот момент курят.
            </p>
            <p>
                <strong>6. Закаливание.</strong>
                Чтобы не болеть простудными заболеваниями, избавить себя от приема
                антибиотиков и других лекарств для их лечения, постоянно закаливайте свой
                организм прохладным воздухом, водой. Но делайте это постепенно,
                ознакомившись с основными правилами и способами закаливания. Не пользуйтесь
                дома теплой одеждой при нормальной комнатной температуре (около 20°С), не
                кутайте себя лишними одеялами во время сна, давайте чаще дышать воздухом
                всему телу, обтирайте его влажным прохладным полотенцем, принимайте
                контрастный душ, ежедневно бывайте на улице (особенно осенью, зимой и
                весной).
            </p>
            <p>
                <strong>7. Не курите!</strong>
                Чаще задумывайтесь о своем здоровье. Как хочется мечтать о долгой и
                счастливой жизни, но курящие в 13 раз чаще заболевают стенокардией, в 12
                раз – инфарктом миокарда, в 10 раз – язвой желудка и в 30 раз – раком
                легких.
            </p>
            <p>
                <strong>8. Не пробуйте наркотические вещества!</strong>
                Каждый знает, что стоит за пристрастием к употреблению наркотиков. Помните,
                что наркотическая зависимость подкрадывается незаметно и начинается с,
                казалось бы, самых безобидных слабостей: попробовать «ради интереса» или
                «за компанию» один–другой раз действие таблетки, порошка, «травки» или
                летучего вещества, чтобы испытать это незнакомое и загадочное состояние
                эйфории, о котором говорят те, кто располагает этими средствами или уже их
                употребляли.
            </p>
            <p>
                <strong>9. Не злоупотребляйте алкоголем!</strong>
                Доказано, чтов человеческом организме нет ни одного органа, который бы не
                разрушался под действием алкоголя. Разрушительное влияние на мозг и печень
                – лишь некоторые из страшных последствий воздействия алкогольных напитков
                на человека. Пиво – это первый легальный наркотик, прокладывающий путь
                другим, более сильным нелегальным наркотическим средствам. Бутылка светлого
                пива объемом 0,5 л эквивалентна 50–60 г водки. Четыре бутылки пива в
                течение дня – 200–240 г водки– почти половина бутылки. Еще хуже ситуация с
                энергетическими коктейлями. Здесь к такому же количеству спиртного
                добавляется кофеин в размере четырех чашек крепкого кофе на банку.
                Хронический алкоголизм развивается в 3–4 раза быстрее от употребления пива,
                чем от употреблениякрепких алкогольных напитков.
            </p>
            <p>
                <strong>10. Личная гигиена.</strong>
                Содержите тело в чистоте: ежедневно принимайте душ и мойте ноги перед сном,
                мойте руки перед каждым приемом пищи, смывайте с тела пот после работы или
                занятий, связанных с физической нагрузкой, в жаркие дни. Утром и вечером
                чистите зубы, следите за состоянием ногтей. Ежедневно пользуйтесь только
                чистым нижним бельем, носками, одеждой, содержите в чистоте постельные
                принадлежности.
            </p>
            <p>
                <strong>11. Ограничьте электромагнитное воздействие на организм! </strong>
                Постоянное длительное воздействие электромагнитного излучения компьютеров,
                ноутбуков, мобильных телефонов, радиоприемников, СВЧ–печей и других
                источниковэлектромагнитного излучения вызывают множество опасных для
                полноценной и продолжительной жизни человека заболеваний. Ограждайте себя
                от постоянного воздействия этих источников. Продолжительность непрерывной
                работы за компьютером без регламентированного перерыва не должна превышать
                2 часов. Через каждые 50 минут работы за компьютером делайте перерывы
                продолжительностью 10–20 минут. Ограничьте частое использование наушников.
                Те, кто постоянно в течение длительного времени (90 минут) слушают через
                наушники громкую музыку (звук превышает 80% возможной громкости) рискуют
                ухудшить или даже потерять слух.
            </p>
            <br clear="all"/>
            <p align="center">
                <strong>4 ОБЩИЕ ПРАВИЛА ЗАНЯТИЙ ФИЗИЧЕСКИМИ УПРАЖНЕНИЯМИ</strong>
            </p>
            <p align="center">
                <strong></strong>
            </p>
            <p>
                1. Если Вы отнесены к подготовительной или специальной медицинской группе
                помните, какие физические упражнения противопоказаны вашему организму или
                имеют ограничения в физических нагрузках.
            </p>
            <p>
                2. Любое занятие физическими упражнениями, предполагающее умеренные, тем
                более повышенные физические нагрузки (на мышцы, связки, суставы,
                сердечно–сосудистую, дыхательную системы) обязательно начинается с 5–10
                минутной разминки; все упражнения на занятии используются, следуя принципу
                постепенного наращивания их интенсивности и напряженности (трудности).
            </p>
            <p>
                3. Оздоровительный и тренировочный эффект использования физических
                упражнений зависит от их регулярности: большие перерывы (4– 5 дней и более)
                между занятиями снижают (или вовсе сглаживают) эффект предыдущих занятий.
            </p>
            <p>
                4. Наибольшая эффективность физических упражнений достигается при занятиях
                на свежем воздухе, в условиях, отвечающих санитарно–гигиеническим нормам,
                соблюдении личной гигиены, чередовании физических нагрузок и отдыха с
                учетом функционального состояния и самочувствия занимающегося, правильной
                организации питания и полноценного отдыха.
            </p>
            <p>
                5. Заниматься интенсивными физическими упражнениями следует не раньше, чем
                через 2 часа после приема пищи и не позднее, чем за час до приема пищи или
                до отхода ко сну.
            </p>
            <p>
                6. После каждого занятия желательно принять душ.
            </p>
            <p>
                7. Заниматься упражнениями на свежем воздухе рекомендуется при температуре
                воздуха не ниже – 20°С. При более низкой температуре занятие переносится в
                помещение.
            </p>
