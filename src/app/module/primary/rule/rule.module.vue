                измерить пульс в положении стоя за 6 с, затем измерить его еще 3 раза в
                положении сидя: в конце 1–й, 2–й и 3–й минут восстановительного периода за
                10 с. Увеличение частоты сердечных сокращений (ЧСС) после нагрузки до 25 %
                оценивается на «отлично», 25–50 % – «хорошо», 51–75 % –
